export function getTextIcon(text: string): string {
  if (!text) return "👤"

  const icons = [
    "👤","👥","🧑","👨","👩","🧔","🧑‍💻","🧑‍🎓","🧑‍🚀","🧑‍🏫",
    "😎","🤓","😄","😁","🙂","😏","😇","🤠","🥸","😈"
    ,"🐵","🐶","🐱","🦁","🐯","🐼","🐨","🐸","🐙","🦄",
    "🐱","🐶","🐭","🐹","🐰","🦊","🐻","🐼","🐨","🐯",
    "🦁","🐮","🐷","🐸","🐵","🦄","🐲","🐔","🐧","🦉",
    "🔥","⚡","💥","💢","✨","🌟","⭐","💫","☄️","🌪️",
    "🧠","💪","🛡️","⚔️","🏹","🧲","🔮","🧪","📡","🧬"
    ,"🎮","🕹️","🎯","🏆","🥇","🥈","🥉","🎖️","🏅","👑",
    "🧙","🧝","🧛","🧟","🧞","🧚","🦸","🦹","🐉","🗡️"
    ,"🌈","☀️","🌙","🌍","🌎","🌏","🌸","🌼","🌺","🍀",
    "💎","🔔","🎵","🎶","💖","💜","💙","💚","🩵","🩷",
    "🦁","🐸","🐵","🦄","🐲","⭐","🌙","☀️","🌈","💎"
  ]

  let hash = 0
  for (let i = 0; i < text.length; i++) {
    hash = (hash << 5) - hash + text.charCodeAt(i)
    hash |= 0
  }

  return icons[Math.abs(hash) % icons.length]
}
